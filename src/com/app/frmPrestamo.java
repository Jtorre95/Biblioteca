    public void retornarPrestamo(){
    if(this.jTable1.isEnabled()){
        int selectedRow = this.jTable1.getSelectedRow();
        Object valueAt = model.getValueAt(selectedRow, 0);
        int idp = Integer.parseInt(valueAt.toString());
        Prestamo art = (Prestamo)st.load(Prestamo.class, idp);
        this.txtid.setText(String.valueOf(art.getIdprestamo()));
        
        this.cblibro.setSelectedItem(art.getIdlibro().getNombre());
         this.cbusuario.setSelectedItem(art.getIdusuario().getNombre());
        this.txtsalida.setText(String.valueOf(art.getFechaSalida()));
        this.txtmaxima.setText(String.valueOf(art.getFechaMaxima()));
        this.txtdevolucion.setText(String.valueOf(art.getFechaDevolucion()));
        
        this.btneditar.setEnabled(true);
        this.btneliminar.setEnabled(true);
        this.btnnuevo.setEnabled(false);
    }    
}
