    public Libros retornoTipo(String desT){
    Libros tipRet = null;
    String desTipo = desT;
    try {
        Query query = st.createQuery("From Libros t Where t. nombre = ?");
        query.setParameter(0, desTipo);
        try {
            tipRet = (Libros)query.uniqueResult();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hay más Libros de tipo: " + desTipo);
        }
        
    } catch (Exception e) {
        
    }
    return tipRet;
}
    
    
   
